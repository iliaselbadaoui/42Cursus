#include "utils.h"

int		equals(t_string s1, t_string s2)
{
	int res;

	res = 0;
	while(*s1 == *s2)
	{
		if (*(s1 + 1) != *(s2 + 1))
		{
			res = 0;
			break ;
		}
		else if (res != 1)
			res = 1;
		s1++;
		s2++;
	}
	return (1);
}